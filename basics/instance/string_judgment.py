"""
字符串判断
"""

str = "i come here"

# 判断所有字符串都是数字或者字母
print(str.isalnum())

# 判断所有字符串都是字母
print(str.isalpha())

# 判断所有字符串都是数字
print(str.isdigit())

# 判断所有字符串都是小写
print(str.islower())

# 判断所有字符串都是大写
print(str.isupper())

# 判断所有字符串都是首字母大写
print(str.istitle())

# 断所有字符都是空白字符、\t、\n、\r
print(str.isspace())